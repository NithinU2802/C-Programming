#include<stdio.h>
#include<string.h>

int main()
{
    int n;
    scanf("%d",&n);
    int a[n][n];
    for(int i=0;i<n;i++)
    for(int j=0;j<n;j++) scanf("%d",&a[i][j]);
    printf("\n");
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            if(n%2!=0){
            if(i==0 || i==n-1 || j==0 || j==n-1 || i==n/2 && j==n/2) printf("%d ",a[i][j]);
            else printf("* ");
            }else{
            if(i==0 || i==n-1 || j==0 || j==n-1 || (j==i && (i==(n/2)-1 || j==n/2)) || 
            ((i==n/2 && j==(n/2)-1) || (i==(n/2)-1 && j==(n/2)) )) printf("%d ",a[i][j]);
            else printf("* ");
            }
            
        }
        printf("\n");
    }
}
